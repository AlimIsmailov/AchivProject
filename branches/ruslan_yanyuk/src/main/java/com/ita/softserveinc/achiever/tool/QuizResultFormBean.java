package com.ita.softserveinc.achiever.tool;

import org.springframework.stereotype.Component;

import com.ita.softserveinc.achiever.entity.Group;
import com.ita.softserveinc.achiever.entity.QuizResult;
import com.ita.softserveinc.achiever.entity.Subtopic;

@Component
public class QuizResultFormBean {

	private QuizResult quizResult;
	private Group group;
	private Subtopic subtopic;
	private int countOfQuestions;

	public QuizResultFormBean() {
	}

	public QuizResultFormBean(QuizResult quizResult) {
		this.quizResult = quizResult;
	}

	public QuizResult getQuizResult() {
		return quizResult;
	}

	public void setQuizResult(QuizResult quizResult) {
		this.quizResult = quizResult;
	}

	public int getCountOfQuestions() {
		return countOfQuestions;
	}

	public void setCountOfQuestions(int countOfQuestions) {
		this.countOfQuestions = countOfQuestions;
	}

	public Group getGroup() {
		return group;
	}

	public void setGroup(Group group) {
		this.group = group;
	}

	public Subtopic getSubtopic() {
		return subtopic;
	}

	public void setSubtopic(Subtopic subtopic) {
		this.subtopic = subtopic;
	}

}
